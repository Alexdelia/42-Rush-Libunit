#include "../inc/atoi_tests.h"

int	atoi_multiple_sign(void)
{
	if (ft_atoi(" ++---+--+-++-+---+42") == atoi(" ++---+--+-++-+---+42"))
		return (RET_OK);
	else
		return (RET_KO);
}
